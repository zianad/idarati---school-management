export type Language = keyof typeof translations;
export type TranslationKey = keyof typeof translations['ar'];

const replaceVars = (text: string, vars: Record<string, string | number>) => {
  let result = text;
  for (const key in vars) {
    result = result.replace(`{${key}}`, String(vars[key]));
  }
  return result;
};

export const getTranslator = (lang: Language) => {
  return (key: TranslationKey, vars?: Record<string, string | number>): string => {
    const text = translations[lang][key] || translations['ar'][key] || key;
    return vars ? replaceVars(text, vars) : text;
  };
};